/* 
Spiegazione:
#include <iostream>: Include la libreria iostream che permette di utilizzare gli oggetti per l'input e l'output, come std::cout per la stampa sulla console.

int main(): La funzione main è il punto di ingresso di un programma C++. È dove inizia l'esecuzione del programma.

std::cout << "Hello, World!" << std::endl;: Questa riga stampa "Hello, World!" sulla console. std::cout è il flusso di output standard, << è l'operatore di inserimento e std::endl è un manipolatore che inserisce una nuova riga.

return 0;: Restituisce 0 al sistema operativo, indicando che il programma è terminato correttamente.
 */

#include <iostream>  // Inclusione della libreria per l'input e l'output

// Funzione principale, punto di ingresso del programma
int main() {
    // Stampa la stringa "Hello, World!" sulla console
    std::cout << "Hello, World!!!" << std::endl;

    // Restituisce 0 per indicare che il programma è terminato correttamente
    return 0;
}